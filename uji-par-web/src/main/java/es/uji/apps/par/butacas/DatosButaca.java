package es.uji.apps.par.butacas;

public class DatosButaca
{
    private int fila;
    private int numero;
    private String seccion;
    private int xFin;
    private int xIni;
    private int yFin;
    private int yIni;

    public DatosButaca()
    {
    }

    public int getFila()
    {
        return fila;
    }

    public void setFila(int fila)
    {
        this.fila = fila;
    }

    public int getNumero()
    {
        return numero;
    }

    public void setNumero(int numero)
    {
        this.numero = numero;
    }

    public String getSeccion()
    {
        return seccion;
    }

    public void setSeccion(String seccion)
    {
        this.seccion = seccion;
    }

    public int getxFin()
    {
        return xFin;
    }

    public void setxFin(int xFin)
    {
        this.xFin = xFin;
    }

    public int getxIni()
    {
        return xIni;
    }

    public void setxIni(int xIni)
    {
        this.xIni = xIni;
    }

    public int getyFin()
    {
        return yFin;
    }

    public void setyFin(int yFin)
    {
        this.yFin = yFin;
    }

    public int getyIni()
    {
        return yIni;
    }

    public void setyIni(int yIni)
    {
        this.yIni = yIni;
    }

}
